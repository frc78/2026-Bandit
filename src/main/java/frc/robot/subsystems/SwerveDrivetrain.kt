    /**
     * Drives to a pose using a PID controller
     *
     * @param target the desired Pose2d to drive to
     * @param threshold distance from target to consider "at target", in meters
     * @param maxRotation the maximum rate of rotation in rad/s
     * @param alwaysMaxSpeed whether the robot should go at max speed for the entire motion
     * @param minSpeed the slowest the robot will move in m/s if fullSpeed is false
     * @param intakeFirst if true, ignores target rotation and instead orients the robot so the
     *   intake (front, 0 degrees) faces toward the target
     * @return true if distance to target is < threshold
     */
    fun driveToPose(
        target: Pose2d,
        threshold: Distance = 0.inches,
        maxSpeed: Double = kSpeedAt12Volts.metersPerSecond,
        maxRotation: Double = MaxAutoRotationSpeed,
        alwaysMaxSpeed: Boolean = false,
        minSpeed: Double = 0.0,
        intakeFirst: Boolean = false,
    ): Boolean {
        Logger.recordOutput("DriveToPose target", target)
        val diff = state.Pose.translation - target.translation
        Logger.recordOutput("DriveToPose angle", diff.angle)
        val targetRotation = if (intakeFirst) (-diff).angle else target.rotation
        FieldCentricFacingAngleAlignments.withTargetDirection(targetRotation)

        Logger.recordOutput("DriveToPose norm", diff.norm)
        val output = posePIDController.calculate(diff.norm)
        Logger.recordOutput("DriveToPose output", output)
        val clampedOutput =
            if (alwaysMaxSpeed) sign(output) * maxSpeed
            else sign(output) * MathUtil.clamp(abs(output), minSpeed, maxSpeed)

        val angle = diff.angle
        val xSpeed = clampedOutput * angle.cos
        val ySpeed = clampedOutput * angle.sin

        val limitedX = xLimiter.calculate(xSpeed)
        val limitedY = yLimiter.calculate(ySpeed)

        setControl(
            FieldCentricFacingAngleAlignments.withVelocityX(limitedX)
                .withVelocityY(limitedY)
                .withMaxAbsRotationalRate(maxRotation)
        )

        return diff.norm < threshold.meters
    }

