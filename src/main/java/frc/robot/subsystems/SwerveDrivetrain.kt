    fun stateTransitions() {
        when (currentState) {
            SwerveState.OrientToHub -> {
                if (!OI.orientToHub()) currentState = SwerveState.FieldCentric
            }

            SwerveState.CardinalSnap -> {
                if (abs(OI.rawRotation()) > 0.05) currentState = SwerveState.FieldCentric
                else if (OI.crossBump()) currentState = SwerveState.CrossBump
                else if (OI.xBrake()) currentState = SwerveState.XBrake
                else if (OI.alignToTower()) currentState = SwerveState.AlignToTower
                else if (OI.snapNorth() || OI.snapEast() || OI.snapSouth() || OI.snapWest())
                    currentState =
                        SwerveState.CardinalSnap // re-triggers setter to update snapTargetDirection
            }
            SwerveState.FieldCentric -> {
                if (OI.crossBump()) currentState = SwerveState.CrossBump
                else if (OI.xBrake()) currentState = SwerveState.XBrake
                else if (OI.alignToTower()) currentState = SwerveState.AlignToTower
                else if (OI.snapNorth() || OI.snapEast() || OI.snapSouth() || OI.snapWest())
                    currentState = SwerveState.CardinalSnap
                else if (OI.orientToHub()) currentState = SwerveState.OrientToHub
            }

            SwerveState.CrossBump -> {
                if (!OI.crossBump()) currentState = SwerveState.FieldCentric
            }

            SwerveState.XBrake -> {
                if (!OI.xBrake()) currentState = SwerveState.FieldCentric
            }

            SwerveState.AlignToTower -> {
                if (!OI.alignToTower()) currentState = SwerveState.FieldCentric
            }
        }
    }

