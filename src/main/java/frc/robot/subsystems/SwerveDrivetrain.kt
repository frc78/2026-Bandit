    fun periodic() {
        /*
         * Periodically try to apply the operator perspective.
         * If we haven't applied the operator perspective before, then we should apply it regardless of DS state.
         * This allows us to correct the perspective in case the robot code restarts mid-match.
         * Otherwise, only check and apply the operator perspective if the DS is disabled.
         * This ensures driving behavior doesn't change until an explicit disable event occurs during testing.
         */
        if (!hasAppliedOperatorPerspective || DriverStation.isDisabled()) {
            DriverStation.getAlliance()
                .ifPresent(
                    Consumer { allianceColor: DriverStation.Alliance? ->
                        setOperatorPerspectiveForward(
                            if (allianceColor == Red) kRedAlliancePerspectiveRotation
                            else kBlueAlliancePerspectiveRotation
                        )
                        hasAppliedOperatorPerspective = true
                    }
                )
        }

        robotInOwnAllianceZone = isRobotInOwnAllianceZone()
        robotInOpponentAllianceZone = isRobotInOpponentAllianceZone()
        Logger.recordOutput("swerve/isRobotInOwnAllianceZone", robotInOwnAllianceZone)
    }

    private fun startSimThread() {
        lastSimTime = Utils.getCurrentTimeSeconds()

        /* Run simulation at a faster rate so PID gains behave more reasonably */
        simNotifier =
            Notifier(
                Runnable {
                    val currentTime = Utils.getCurrentTimeSeconds()
                    val deltaTime = currentTime - lastSimTime
                    lastSimTime = currentTime

                    /* use the measured time delta, get battery voltage from WPILib */
                    updateSimState(deltaTime, RobotController.getBatteryVoltage())
                }
            )
        simNotifier.startPeriodic(kSimLoopPeriod)
    }

